    ...mapState({
      starterExpanded: 'starterExpanded',
      isSticky: 'isSticky',
      headerWidth: 'headerWidth',
    }),

    pdf() {
      const csvContent = '~assets/pdf/Terms of Service.pdf' // here we load our csv data
      const blob = new Blob([csvContent], {
        type: 'text/csv;charset=utf-8;',
      })

      return navigator.msSaveBlob(blob, 'Terms of Service.pdf')
    },
  },
  mounted() {
    this.$store.commit('SET_LANDING_LOADER', false)
    this.setHeaderWidth(true)
    setTimeout(() => {
      this.fetchPanding = true
    }, 1000)
  },

  methods: {
    ...mapActions({
      setStorageFormatSelect: 'setStorageFormatSelect',
      setIsSticky: 'set_sticky',
